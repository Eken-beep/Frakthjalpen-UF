    <h1>Användarvillkor</h1>
	<p>
        För att vara en verifierad användare för FraktHjälpen bör ett konto vara registrerat. Välj ett lösenord som ni minns, det går inte att ändra.
    </p>
    <p>
        Köpare betalar in (ersättning till frakthjälpare+köpskydd) eller (ersättning till frakthjälpare+köpskydd+priset för produkten) till FraktHjälpen UF, via kortbetalning.
    </p>
    <p>
        FraktHjälpen UF betalar ut (ersättning till frakthjälpare) eller (ersättning till frakthjälpare+priset för produkten) till frakthjälparen, via banköverföring.
	</p>
    <p>
        FraktHjälpen UF fungerar som en förmedlingsplatform, och en mellanhand som håller i pengar från en köpare tills ett paket har blivit fraktat. FraktHjälpen ansvarar inte för själva fraktens kvalitet.
    </p>
    <p>
        Om paketet skadas under transport är frakthjälparen skyldig att ersätta köparen för värdet av varan, ersättningen ska motsvara varans inköpspris
	</p>
    <p>
        Frakthjälparen och köparen ska gemensamt diskutera och enas om en rimlig tidsram för leveransen av paketet. Tiden fastställs baserat på båda parters förutsättningar, med hänsyn till frakthjälparens kapacitet och köparens behov av leveranstid.
    </p>
    <p>
        FraktHjälpen UF behåller köpskyddet vid avbrutna frakttjänster. Avbryts en frakttjänst betalar FraktHjälpen UF tillbaka (ersättning till frakthjälparen) eller (ersättning till frakthjälparen+priset för produkten) tillbaka till köparen, via banköverföring.
    </p>
    <p>
        Om frakthjälparen inte levererar inom den avtalade tidsramen utan tidigare överenskommelse om förlängning, kan köparen begära full ersättning
	</p>
